import type { IsosurfaceMesh } from '../types';

/**
 * Generate isosurface mesh using the Marching Cubes algorithm
 * Paul Bourke convention: v < isovalue -> bit ON (outside)
 * Normals computed from scalar field gradient (smooth shading)
 */
export function marchingCubes(
  field: Float64Array,
  nx: number, ny: number, nz: number,
  isovalue: number,
  origin: [number, number, number],
  spacing: number,
): IsosurfaceMesh {
  const vertices: number[] = [];
  const normals: number[] = [];
  const indices: number[] = [];

  // For positive isosurface, gradient points inward (toward lobe center).
  // Negate to get outward-pointing normals consistent with triangle winding.
  const nSign = isovalue >= 0 ? -1 : 1;

  function fieldVal(ix: number, iy: number, iz: number): number {
    return field[iz * ny * nx + iy * nx + ix];
  }

  // Scalar field gradient (central differences) -> isosurface normal direction
  function fieldGradient(wx: number, wy: number, wz: number): [number, number, number] {
    // World coords -> grid coords
    const gx = (wx - origin[0]) / spacing;
    const gy = (wy - origin[1]) / spacing;
    const gz = (wz - origin[2]) / spacing;
    const ix = Math.min(Math.max(Math.round(gx), 1), nx - 2);
    const iy = Math.min(Math.max(Math.round(gy), 1), ny - 2);
    const iz = Math.min(Math.max(Math.round(gz), 1), nz - 2);
    const dx = (fieldVal(ix + 1, iy, iz) - fieldVal(ix - 1, iy, iz)) / (2 * spacing);
    const dy = (fieldVal(ix, iy + 1, iz) - fieldVal(ix, iy - 1, iz)) / (2 * spacing);
    const dz = (fieldVal(ix, iy, iz + 1) - fieldVal(ix, iy, iz - 1)) / (2 * spacing);
    const len = Math.sqrt(dx * dx + dy * dy + dz * dz);
    if (len < 1e-20) return [0, 0, 1];
    return [dx / len, dy / len, dz / len];
  }

  function interpolate(
    p1: [number, number, number], v1: number,
    p2: [number, number, number], v2: number,
  ): [number, number, number] {
    if (Math.abs(isovalue - v1) < 1e-15) return p1;
    if (Math.abs(isovalue - v2) < 1e-15) return p2;
    if (Math.abs(v1 - v2) < 1e-15) return p1;
    const t = (isovalue - v1) / (v2 - v1);
    return [
      p1[0] + t * (p2[0] - p1[0]),
      p1[1] + t * (p2[1] - p1[1]),
      p1[2] + t * (p2[2] - p1[2]),
    ];
  }

  const EDGE_VERTS: [number, number][] = [
    [0, 1], [1, 2], [2, 3], [3, 0],
    [4, 5], [5, 6], [6, 7], [7, 4],
    [0, 4], [1, 5], [2, 6], [3, 7],
  ];

  for (let iz = 0; iz < nz - 1; iz++) {
    for (let iy = 0; iy < ny - 1; iy++) {
      for (let ix = 0; ix < nx - 1; ix++) {
        const v = [
          fieldVal(ix,     iy,     iz),
          fieldVal(ix + 1, iy,     iz),
          fieldVal(ix + 1, iy + 1, iz),
          fieldVal(ix,     iy + 1, iz),
          fieldVal(ix,     iy,     iz + 1),
          fieldVal(ix + 1, iy,     iz + 1),
          fieldVal(ix + 1, iy + 1, iz + 1),
          fieldVal(ix,     iy + 1, iz + 1),
        ];

        // Paul Bourke convention: v < isovalue -> bit ON
        let cubeIndex = 0;
        if (v[0] < isovalue) cubeIndex |= 1;
        if (v[1] < isovalue) cubeIndex |= 2;
        if (v[2] < isovalue) cubeIndex |= 4;
        if (v[3] < isovalue) cubeIndex |= 8;
        if (v[4] < isovalue) cubeIndex |= 16;
        if (v[5] < isovalue) cubeIndex |= 32;
        if (v[6] < isovalue) cubeIndex |= 64;
        if (v[7] < isovalue) cubeIndex |= 128;

        const edgeBits = EDGE_TABLE[cubeIndex];
        if (edgeBits === 0) continue;

        const p: [number, number, number][] = [
          [origin[0] + ix * spacing,       origin[1] + iy * spacing,       origin[2] + iz * spacing],
          [origin[0] + (ix + 1) * spacing, origin[1] + iy * spacing,       origin[2] + iz * spacing],
          [origin[0] + (ix + 1) * spacing, origin[1] + (iy + 1) * spacing, origin[2] + iz * spacing],
          [origin[0] + ix * spacing,       origin[1] + (iy + 1) * spacing, origin[2] + iz * spacing],
          [origin[0] + ix * spacing,       origin[1] + iy * spacing,       origin[2] + (iz + 1) * spacing],
          [origin[0] + (ix + 1) * spacing, origin[1] + iy * spacing,       origin[2] + (iz + 1) * spacing],
          [origin[0] + (ix + 1) * spacing, origin[1] + (iy + 1) * spacing, origin[2] + (iz + 1) * spacing],
          [origin[0] + ix * spacing,       origin[1] + (iy + 1) * spacing, origin[2] + (iz + 1) * spacing],
        ];

        const vertList: ([number, number, number] | null)[] = new Array(12).fill(null);
        for (let e = 0; e < 12; e++) {
          if (edgeBits & (1 << e)) {
            const [a, b] = EDGE_VERTS[e];
            vertList[e] = interpolate(p[a], v[a], p[b], v[b]);
          }
        }

        const row = cubeIndex * 16;
        for (let t = 0; TRI_TABLE_FLAT[row + t] !== -1; t += 3) {
          const e0 = TRI_TABLE_FLAT[row + t];
          const e1 = TRI_TABLE_FLAT[row + t + 1];
          const e2 = TRI_TABLE_FLAT[row + t + 2];
          const vt0 = vertList[e0];
          const vt1 = vertList[e1];
          const vt2 = vertList[e2];
          if (!vt0 || !vt1 || !vt2) continue;

          const baseIdx = vertices.length / 3;
          vertices.push(vt0[0], vt0[1], vt0[2]);
          vertices.push(vt1[0], vt1[1], vt1[2]);
          vertices.push(vt2[0], vt2[1], vt2[2]);

          // Gradient-based normals (flipped by nSign for correct outward direction)
          const n0 = fieldGradient(vt0[0], vt0[1], vt0[2]);
          const n1 = fieldGradient(vt1[0], vt1[1], vt1[2]);
          const n2 = fieldGradient(vt2[0], vt2[1], vt2[2]);
          normals.push(nSign * n0[0], nSign * n0[1], nSign * n0[2]);
          normals.push(nSign * n1[0], nSign * n1[1], nSign * n1[2]);
          normals.push(nSign * n2[0], nSign * n2[1], nSign * n2[2]);

          indices.push(baseIdx, baseIdx + 1, baseIdx + 2);
        }
      }
    }
  }

  return {
    vertices: new Float32Array(vertices),
    normals: new Float32Array(normals),
    indices: new Uint32Array(indices),
  };
}

